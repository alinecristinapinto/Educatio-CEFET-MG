package ManutencaoDiarios.Modelo;

/**
 *
 * @author Felipe
 */
public class Turma {
    private int id;
    private String nome;
    private int serie;

    public Turma() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getSerie() {
        return serie;
    }

    public void setSerie(int serie) {
        this.serie = serie;
    }
    
}
